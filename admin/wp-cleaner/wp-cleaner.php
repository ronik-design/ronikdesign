if($dev_general_settings['rss_feed_links']){
    remove_action('wp_head', 'feed_links', 2); 
    remove_action('wp_head', 'feed_linRonik_extra', 3); 
}

// Disable WordPress emojis
if($dev_general_settings['disable_wordpress_em']){
    remove_action('wp_head', 'print_emoji_detection_script', 7);
    remove_action('admin_print_scripts', 'print_emoji_detection_script');
    remove_action('wp_print_styles', 'print_emoji_styles');
    remove_action('admin_print_styles', 'print_emoji_styles');
    remove_filter('the_content_feed', 'wp_staticize_emoji');
    remove_filter('comment_text_rss', 'wp_staticize_emoji');
    remove_filter('wp_mail', 'wp_staticize_emoji_for_email');
    add_filter('emoji_svg_url', '__return_false', 10, 2);
}

// Disable comments & Pings & Trackbacks
function Ronik_disable_comments_post_types_support() {
    $post_types = get_post_types();
    foreach ($post_types as $post_type) {
        if (post_type_supports($post_type, 'comments')) {
            remove_post_type_support($post_type, 'comments');
            remove_post_type_support($post_type, 'trackbacks');
        }
    }
}
function Ronik_disable_comments_admin_menu() {
    remove_menu_page('edit-comments.php');
    remove_submenu_page('options-general.php', 'options-discussion.php');
}
function Ronik_disable_comments_admin_bar($wp_admin_bar) {
    $wp_admin_bar->remove_node('comments');
}
function Ronik_disable_comments_admin_redirect() {
    global $pagenow;
    if ($pagenow == 'edit-comments.php' || $pagenow == 'options-discussion.php') {
        wp_redirect(admin_url());
        exit;
    }
}
function Ronik_disable_comments_dashboard() {
    remove_meta_box('dashboard_recent_comments', 'dashboard', 'normal');
}

// Remove Site Icon control from Theme Customization
if($dev_general_settings['remove_site_icon_control']){
    function Ronik_customize_register($wp_customize) {
        $wp_customize->remove_control('site_icon');
    }
    add_action('customize_register', 'Ronik_customize_register', 20);  
}

// Disable search
if($dev_general_settings['disable_search']){
    function Ronik_disable_search($query, $error = true) {
        if (is_search() && !is_admin()) {
            $query->is_search = false;
            $query->query_vars[s] = false;
            $query->query[s] = false;
            if ($error == true) {
                $query->is_404 = true;
            }
        }
    }
    add_action('parse_query', 'Ronik_disable_search');
    add_filter('get_search_form', '__return_null');
}
if($dev_general_settings['ronik_tinymce_disable_emojis']){
    function Ronik_tinymce_disable_emojis($plugins) {
        if (is_array($plugins)) {
            return array_diff($plugins, array('wpemoji'));
        } else {
            return array();
        }
    }
    add_filter('tiny_mce_plugins', 'Ronik_tinymce_disable_emojis'); // disable wpemoji TinyMCE plugin
}

// Enable TinyMCE paste as text by default
if($dev_general_settings['ronik_tinymce_paste_as_text']){
    function Ronik_tinymce_paste_as_text($init) {
        $init['paste_as_text'] = true;
        return $init;
    }
    add_filter('tiny_mce_before_init', 'Ronik_tinymce_paste_as_text');
}