<?php

$dev_general_settings = get_field('wp_cleaner_settings', 'options');

// Hide Posts from admin
if($dev_general_settings['hide_posts_from_admin']){
    function Ronik_disable_posts_admin_menu() {
        remove_menu_page('edit.php');
    }
    add_action('admin_menu', 'Ronik_disable_posts_admin_menu'); // remove posts link from admin menu
    function Ronik_disable_posts_admin_bar() {
        global $wp_admin_bar;
        $wp_admin_bar->remove_menu('new-post');
    }
    add_action('wp_before_admin_bar_render', 'Ronik_disable_posts_admin_bar'); // remove new post link from admin bar
}

// Unregister default taxonomies for Posts
if($dev_general_settings['unregister_default_taxonomies_posts']){
    function Ronik_unregister_default_taxonomies() {
        unregister_taxonomy_for_object_type('category', 'post'); // unregister categories for posts
        unregister_taxonomy_for_object_type('post_tag', 'post'); // unregister tags for posts
    }
    add_action('init', 'Ronik_unregister_default_taxonomies');
}

// Disable Dashicons on Front-end
if($dev_general_settings['wpdocs_dequeue_dashicon']){
    function wpdocs_dequeue_dashicon() {
        if (current_user_can( 'update_core' )) {
            return;
        }
        wp_deregister_style('dashicons');
    }
    add_action( 'wp_enqueue_scripts', 'wpdocs_dequeue_dashicon' );
}

//Disable Self Pingback
if($dev_general_settings['disable_pingback']){
    function disable_pingback( &$links ) {
        foreach ( $links as $l => $link )
        if ( 0 === strpos( $link, get_option( 'home' ) ) )
        unset($links[$l]);
    }
    add_action( 'pre_ping', 'disable_pingback' );
}

// Dequeue WP Block Editor styles
if($dev_general_settings['ronik_dequeue_block_styles']){
    function Ronik_dequeue_block_styles(){
        wp_dequeue_style('wp-block-library');
    }
    add_action('wp_enqueue_scripts', 'Ronik_dequeue_block_styles', 100);
}

// Mitigate information leakage vulnerability
if($dev_general_settings['mitigate_vulnerability']){
    remove_action( 'wp_head', 'wp_generator' ) ;
}

// Remove oEmbed discovery links and REST API endpoint
if($dev_general_settings['remove_oEmbed_discovery']){
    remove_action('wp_head', 'wp_oembed_add_discovery_links');
    remove_action('rest_api_init', 'wp_oembed_register_route');
}

// This file removes and cleans up the unnecessary files generated by wp by default.
if($dev_general_settings['remove_cleanup_files']){
    add_action('admin_bar_menu', 'Ronik_disable_comments_admin_bar', 999); // remove comments links from admin bar
    add_action('admin_menu', 'Ronik_disable_comments_admin_menu'); // remove comments and discussion settings from admin menu
    add_action('admin_init', 'Ronik_disable_comments_post_types_support'); // disable support for comments and trackbacks for all post types
    add_filter('comments_open', '__return_false', 20); // close comments
    add_filter('pings_open', '__return_false', 20); // close pings
    add_filter('comments_array', '__return_empty_array'); // return empty comments array
    add_action('admin_init', 'Ronik_disable_comments_admin_redirect'); // redirect any user trying to access comments page
    add_action('admin_init', 'Ronik_disable_comments_dashboard'); // remove comments metabox from dashboard
}

/**
